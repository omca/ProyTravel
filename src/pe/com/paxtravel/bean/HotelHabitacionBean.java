package pe.com.paxtravel.bean;

public class HotelHabitacionBean {
	
	private String idCotiza;
	private int idTipoHabitacion;
	private int nuHabitacion;
	
	public String getIdCotiza() {
		return idCotiza;
	}
	public void setIdCotiza(String idCotiza) {
		this.idCotiza = idCotiza;
	}
	public int getIdTipoHabitacion() {
		return idTipoHabitacion;
	}
	public void setIdTipoHabitacion(int idTipoHabitacion) {
		this.idTipoHabitacion = idTipoHabitacion;
	}
	public int getNuHabitacion() {
		return nuHabitacion;
	}
	public void setNuHabitacion(int nuHabitacion) {
		this.nuHabitacion = nuHabitacion;
	}

}
